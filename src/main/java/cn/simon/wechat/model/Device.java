package cn.simon.wechat.model;

public class Device {

	private String deviceId;

	private String gateWayId;

	private String deviceTypeId;

	private String ieeeAddress;

	private String cname;

	private String deviceTypeCategoryId;

	private String actived;

	private String bindStatusId;

	private String lockStatusId;

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public String getGateWayId() {
		return gateWayId;
	}

	public void setGateWayId(String gateWayId) {
		this.gateWayId = gateWayId;
	}

	public String getDeviceTypeId() {
		return deviceTypeId;
	}

	public void setDeviceTypeId(String deviceTypeId) {
		this.deviceTypeId = deviceTypeId;
	}

	public String getIeeeAddress() {
		return ieeeAddress;
	}

	public void setIeeeAddress(String ieeeAddress) {
		this.ieeeAddress = ieeeAddress;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getDeviceTypeCategoryId() {
		return deviceTypeCategoryId;
	}

	public void setDeviceTypeCategoryId(String deviceTypeCategoryId) {
		this.deviceTypeCategoryId = deviceTypeCategoryId;
	}

	public String getActived() {
		return actived;
	}

	public void setActived(String actived) {
		this.actived = actived;
	}

	public String getBindStatusId() {
		return bindStatusId;
	}

	public void setBindStatusId(String bindStatusId) {
		this.bindStatusId = bindStatusId;
	}

	public String getLockStatusId() {
		return lockStatusId;
	}

	public void setLockStatusId(String lockStatusId) {
		this.lockStatusId = lockStatusId;
	}

}
